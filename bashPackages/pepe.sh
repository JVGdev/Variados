
pepe()(
if [ $# -gt 0 ]; then

	if [ $1 != '-n' ]; then
		cat << "EOF"

                   @=====@@      @@                         
                @=====@**%%@@@======@@@                     
               ===%=======@@@@@@@=======@@@-                
              =======@==@        @@=@@      @=              
             @======        @@@@   @    @@@@  @             
            ==========@    @@: @   @    @  @  @             
          @=============@    @    @@=@                      
          ===============@=======%==.===@@@@@               
         @==============#@*%**.=======#@====@               
         @=====@=*.*@========================@              
         @======@.........@@==============@-.*              
         @=======@..++++=.................@                 
          =========@...++@**+++++++++     ...               
           @==========....=   -=++++@      ..@              
              ===========-..................                
                 @===========@@..@========@                 
                     @===============@@                                                                                                  
 
 ----------------------------------------------------------

EOF
	else 
		shift
	fi
else
		cat << "EOF"

                   @=====@@      @@                         
                @=====@**%%@@@======@@@                     
               ===%=======@@@@@@@=======@@@-                
              =======@==@        @@=@@      @=              
             @======        @@@@   @    @@@@  @             
            ==========@    @@: @   @    @  @  @             
          @=============@    @    @@=@                      
          ===============@=======%==.===@@@@@               
         @==============#@*%**.=======#@====@               
         @=====@=*.*@========================@              
         @======@.........@@==============@-.*              
         @=======@..++++=.................@                 
          =========@...++@**+++++++++     ...               
           @==========....=   -=++++@      ..@              
              ===========-..................                
                 @===========@@..@========@                 
                     @===============@@                                                                                                  
 
 ----------------------------------------------------------

EOF
fi
	for file in $@; do
		filename=`basename -s .cpp ${file}`
		g++ -o ${filename} ${filename}.cpp
		./${filename}
	done
)
