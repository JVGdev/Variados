
pepe()(
print=1

while test $# -gt 0; do
	case $1 in 
		-n|--no-pepe)
			$print=0
		shift
		
		;;
		-c|--code)
			echo "
include <iostream>

using namespace std;

int main(){

	return 0;
}

" >> $2.cpp
	
			echo "Clase $2 criada!!!"

			return
		;;
		*)
			break
	esac
done

if [ $print -gt 0 ]; then
cat << "EOF"

                   @=====@@      @@                         
                @=====@**%%@@@======@@@                     
               ===%=======@@@@@@@=======@@@-                
              =======@==@        @@=@@      @=              
             @======        @@@@   @    @@@@  @             
            ==========@    @@: @   @    @  @  @             
          @=============@    @    @@=@                      
          ===============@=======%==.===@@@@@               
         @==============#@*%**.=======#@====@               
         @=====@=*.*@========================@              
         @======@.........@@==============@-.*              
         @=======@..++++=.................@                 
          =========@...++@**+++++++++     ...               
           @==========....=   -=++++@      ..@              
              ===========-..................                
                 @===========@@..@========@                 
                     ===============@@                                                                                                  
 
 ----------------------------------------------------------

EOF
fi
	for file in $@; do
		filename=`basename -s .cpp ${file}`
		g++ -o ${filename} ${filename}.cpp -O3
		./${filename}
	done
shift "$((OPTIND -1))"
)
